            "  }",
            "}",
            "customElements.define('my-card', MyCard);",
            "```",
            "",
            "### Web Components patterns",
            "- Use `ShadowRoot` for style encapsulation; `:host` for the host element, `::slotted()` for projected content",
            "- Use `<slot>` for content projection; `slotchange` event for dynamic slot content",
            "- Lifecycle callbacks: `connectedCallback`, `disconnectedCallback`, `attributeChangedCallback`, `adoptedCallback`",
            "- Use `observedAttributes` static getter to react to attribute changes",
            "- Prefer `formAssociated` static getter + `ElementInternals` for form-integrated custom elements",
            "",
            "## Canvas and Web APIs",
            "",
            "```html",
            "<canvas id=\"chart\" width=\"600\" height=\"400\">",
            "  <p>Fallback content for non-canvas browsers.</p>",
            "</canvas>",
            "```",
            "",
            "```javascript",
            "const ctx = document.getElementById('chart').getContext('2d');",
            "ctx.fillStyle = '#4f46e5';",
