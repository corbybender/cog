            "```",
            "",
            "### Image best practices",
            "- Always include `width` and `height` to prevent layout shift (CLS)",
            "- Use `loading=\"lazy\"` for below-fold images; keep hero images eager",
            "- `decoding=\"async\"` for off-screen images",
            "- Provide `alt` text that describes the image's purpose, not its appearance",
            "- Use `<picture>` for format negotiation (WebP/AVIF fallback to JPEG)",
            "- Use `srcset` + `sizes` for resolution/density switching",
            "",
            "## Web Components",
            "",
            "```html",
            "<template id=\"my-card\">",
            "  <style>",
            "    :host { display: block; border: 1px solid #ddd; border-radius: 8px; padding: 1rem; }",
            "    ::slotted(h2) { margin: 0 0 0.5rem; }",
            "  </style>",
            "  <slot name=\"header\"></slot>",
            "  <slot></slot>",
            "</template>",
            "```",
            "",
            "```javascript",
            "class MyCard extends HTMLElement {",
            "  constructor() {",
