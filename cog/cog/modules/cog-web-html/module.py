            "",
            "## HTML Performance",
            "",
            "- Inline critical CSS; defer non-critical stylesheets with `media=\"print\" onload=\"this.media='all'\"`",
            "- Use `<script type=\"module\">` for automatic defer; use `async` for independent scripts",
            "- Preload critical resources: `<link rel=\"preload\" href=\"font.woff2\" as=\"font\" crossorigin />`",
            "- Preconnect to origins: `<link rel=\"preconnect\" href=\"https://cdn.example.com\" />`",
            "- Minimize DOM depth; avoid deep nesting (>15 levels)",
            "- Use `fetchpriority=\"high\"` on LCP images and `fetchpriority=\"low\"` on below-fold resources",
            "- Reduce CLS: always set `width`/`height` on images and videos; reserve space for dynamic content",
            "",
            "## Structured Data (Schema.org)",
            "",
            "```html",
