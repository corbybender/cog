            "",
            "ctx.beginPath();",
            "ctx.arc(300, 200, 50, 0, Math.PI * 2);",
            "ctx.fill();",
            "```",
            "",
            "### Key Web APIs",
            "- **Intersection Observer**: lazy-load images and trigger scroll-based animations without scroll listeners",
            "- **Resize Observer**: respond to element size changes for responsive components",
            "- **Mutation Observer**: watch DOM changes for dynamic content",
            "- **Clipboard API**: `navigator.clipboard.writeText()` / `navigator.clipboard.readText()`",
