            "- Truly reactive with compile-time dependency tracking",
            "- Built-in scoped styles per component",
            "",
            "### Reactive Declarations",
            "- $: reactive statement — re-runs when dependencies change",
            "- $: doubled = count * 2 — reactive assignment",
            "- $: if (count > 10) { ... } — reactive side effect",
            "- let declarations in script are reactive by default",
            "- $state rune (Svelte 5) for explicit reactive state",
            "- $derived rune for computed values (Svelte 5)",
            "- $effect rune for side effects (Svelte 5)",
            "- Reactivity tracked at compile time, no runtime overhead",
            "",
            "### Components",
            "- .svelte files with <script>, <template>, <style> sections",
