            "- Event dispatching with createEventDispatcher or callback props",
            "- Slot for content projection, named slots for multiple areas",
            "- {#if}, {:else if}, {:else}, {/if} for conditionals",
            "- {#each items as item, i} for list rendering",
            "- {#await promise} for async template blocks",
            "- {@html rawHtml} for unescaped HTML insertion",
            "- Bindings: bind:value, bind:checked, bind:this, bind:clientWidth",
            "",
            "### Stores",
            "- Writable stores: writable(initialValue)",
            "- Readable stores: readable(initialValue, start function)",
            "- Derived stores: derived(store(s), callback)",
