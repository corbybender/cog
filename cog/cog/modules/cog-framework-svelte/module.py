            "- store.set(value) and store.update(fn) for writable stores",
            "- Custom stores: return subscribe + any methods",
            "- Stores work across components without prop drilling",
            "- Svelte 5: $state in .svelte.ts files replaces stores for simple cases",
            "",
            "### SvelteKit Routing",
            "- File-based routing under src/routes/",
            "- src/routes/+page.svelte — page component",
            "- src/routes/+page.ts — load function (runs on server + client)",
            "- src/routes/+page.server.ts — server-only load function",
            "- src/routes/+layout.svelte — shared layout",
            "- src/routes/+layout.ts — shared data loading",
            "- src/routes/+error.svelte — error page",
            "- Dynamic params: [slug], [...rest], [[optional]]",
            "- Route groups with (group) parentheses",
            "",
            "### Load Functions",
            "- export const load = async ({ params, fetch, url, parent }) => {}",
            "- +page.ts load runs on both server and client",
            "- +page.server.ts load runs only on the server",
            "- Return plain objects as page data",
            "- Access parent layout data with parent()",
            "- Use fetch() for SSR-safe API calls (not global fetch)",
            "- depends() for fine-grained cache invalidation",
            "- Type-safe with PageLoad and PageServerLoad types",
            "",
            "### Form Actions",
            "- Defined in +page.server.ts with actions object",
            "- export const actions = { default: async ({ request }) => {} }",
            "- Named actions: export const actions = { create: async ..., delete: async ... }",
