class NodeLintTool(Tool):
    name = "nodejs.lint"
    description = "Lint Node.js code"
    required_permissions = ["shell.execute"]

    def execute(self, path: str = ".", fix: bool = False, **kwargs) -> ToolResult:
        from cog.tools.shell import ShellTool

        shell = ShellTool()
        tool = "eslint" if not fix else "eslint --fix"
        command = f"{tool} {path}"
        result = shell.execute(command=command, timeout=60)
        return result


class NodeSyntaxVerifier(Verifier):
    name = "nodejs.syntax"
    description = "Verify Node.js syntax"

    def verify(self, target, **kwargs) -> VerificationResult:
        from cog.tools.shell import ShellTool

        shell = ShellTool()
        result = shell.execute(
            command=f"node --check {target}", timeout=30
        )
        if result.success:
            return VerificationResult(
                verifier=self.name,
                status=VerificationStatus.PASSED,
                message=f"Node.js syntax OK: {target}",
            )
        return VerificationResult(
            verifier=self.name,
            status=VerificationStatus.FAILED,
            message=f"Node.js syntax error in {target}",
            details={"error": result.error},
        )


