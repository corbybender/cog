class NpmInstallTool(Tool):
    name = "nodejs.npm.install"
    description = "Install npm packages"
    required_permissions = ["shell.execute"]

    def execute(self, packages: str, save: bool = True, dev: bool = False, **kwargs) -> ToolResult:
        from cog.tools.shell import ShellTool

        shell = ShellTool()
        flags = []
        if save:
            flags.append("--save")
        if dev:
            flags.append("--save-dev")
        
        command = f"npm install {' '.join(flags)} {packages}"
        result = shell.execute(command=command, timeout=300)
        return result


